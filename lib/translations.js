/**
 * Get translation for a key in specified language
 * @param {string} key - Translation key
 * @param {string} lang - Language code ('en' or 'ur')
 * @returns {string} Translated text or key if not found
 */
export function t(key, lang = 'en') {
    return translations[lang]?.[key] || translations['en']?.[key] || key;
}

/**
 * Get text direction for language
 * @param {string} lang - Language code
 * @returns {string} 'rtl' for Urdu, 'ltr' for English
 */
export function getDirection(lang) {
    return lang === 'ur' ? 'rtl' : 'ltr';
}

/**
 * Format number based on language
 * @param {number} num - Number to format
 * @param {string} lang - Language code
 * @returns {string} Formatted number
 */
export function formatNumber(num, lang = 'en') {
    if (lang === 'ur') {
        // Convert to Urdu-Indic numerals
        const urduNumerals = ['۰', '۱', '۲', '۳', '۴', '۵', '۶', '۷', '۸', '۹'];
        return num.toString().split('').map(digit => {
            return /\d/.test(digit) ? urduNumerals[parseInt(digit)] : digit;
        }).join('');
    }
    return num.toLocaleString('en-US');
}

/**
 * Format currency based on language
 * @param {number} amount - Amount to format
 * @param {string} lang - Language code
 * @returns {string} Formatted currency
 */
export function formatCurrency(amount, lang = 'en') {
    const formatted = amount.toLocaleString('en-US', {
        minimumFractionDigits: 2,
        maximumFractionDigits: 2
    });
    
    if (lang === 'ur') {
        return `روپے ${formatNumber(amount, 'ur')}`;
    }
    return `PKR ${formatted}`;
}

/**
 * Format date based on language
 * @param {Date} date - Date object
 * @param {string} lang - Language code
 * @returns {string} Formatted date
 */
export function formatDate(date, lang = 'en') {
    if (!(date instanceof Date)) {
        date = new Date(date);
    }
    
    if (lang === 'ur') {
        const urduMonths = [
            'جنوری', 'فروری', 'مارچ', 'اپریل', 'مئی', 'جون',
            'جولائی', 'اگست', 'ستمبر', 'اکتوبر', 'نومبر', 'دسمبر'
        ];
        const day = formatNumber(date.getDate(), 'ur');
        const month = urduMonths[date.getMonth()];
        const year = formatNumber(date.getFullYear(), 'ur');
        return `${day} ${month} ${year}`;
    }
    
    return date.toLocaleDateString('en-US', {
        year: 'numeric',
        month: 'long',
        day: 'numeric'
    });
}

